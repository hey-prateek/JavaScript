// === node.style Example ===
// Suppose HTML: <h2 id="title">Hello DOM Styling</h2>

let heading = document.getElementById("title");

// Apply inline styles dynamically
heading.style.color = "crimson";
heading.style.backgroundColor = "lightyellow";
heading.style.fontSize = "30px";
heading.style.padding = "10px";
heading.style.borderRadius = "8px";

console.log("Dynamic style applied!");
